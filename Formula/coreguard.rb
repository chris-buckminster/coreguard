    <<~EOS
      To start coreguard as a DNS blocker, you need root privileges:

        sudo coreguard install

      This installs a launchd service that starts coreguard on boot.

      Alternatively, run manually:

        sudo coreguard start

      To manage the Homebrew service (runs as root):

        sudo brew services start coreguard

      Config directory: #{etc}/coreguard/
