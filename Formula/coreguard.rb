  def post_install
    (etc/"coreguard").mkpath
    (etc/"coreguard/blocklists").mkpath

    touch etc/"coreguard/custom-allow.txt" unless (etc/"coreguard/custom-allow.txt").exist?
    touch etc/"coreguard/custom-block.txt" unless (etc/"coreguard/custom-block.txt").exist?
  end

